        <h1>What is UPSC?</h1>
        <p>UPSC is India’s central agency which conducts exams like Civil Services Exam (CSE) to recruit candidates into top government services like IAS, IPS, IFS etc. UPSC recruits candidates to both civil services as well as defence services.</p>
        
        <h2>What is the full form of UPSC?</h2>
        <p>The full form of UPSC is Union Public Service Commission.</p>
        
        <h2>What are the exams conducted by UPSC for selection into civil services?</h2>
        <ul>
            <li>Civil Services Examination (CSE)</li>
            <li>Engineering Services Examination (ESE)</li>
            <li>Indian Forestry Services Examination (IFoS)</li>
            <li>Central Armed Police Forces Examination (CAPF)</li>
            <li>Indian Economic Service and Indian Statistical Service (IES/ISS)</li>
            <li>Combined Geo-Scientist and Geologist Examination</li>
            <li>Combined Medical Services (CMS)</li>
            <li>Special Class Railway Apprentices Exam (SCRA)</li>
            <li>Limited Departmental Competitive Examination for selection of Assistant Commandant. (Executive) in CISF</li>
        </ul>

        <h2>What are the exams conducted by UPSC for selection into defence services?</h2>
        <ul>
            <li>National Defence Academy & Naval Academy Examination – NDA & NA (I)</li>
            <li>National Defence Academy & Naval Academy Examination – NDA & NA (II)</li>
            <li>Combined Defense Services Exam – CDS (I)</li>
            <li>Combined Defense Services Exam – CDS (II)</li>
        </ul>

        <h2>UPSC Exam: What is the UPSC Civil Services Examination?</h2>
        <p>Civil Services Examination (CSE) is one of the most popular exams conducted by the Union Public Service Commission. It is widely known as the ‘IAS exam’ even though CSE is a common exam to recruit candidates to about 24 top government services like IAS, IPS, IFS, IRS etc.</p>
        
        <p>UPSC Civil Services Exam (CSE) consists of 3 stages. The stages are:</p>
        <ol>
            <li>Preliminary Exam (objective)</li>
            <li>Main Exam (written)</li>
            <li>Interview (personality test)</li>
        </ol>
        
        <p>The exam window extends around 10-12 months (usually from June month of a year to June month next year when the results are announced).</p>

