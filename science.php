    <h2>Science Stream After 10th: Subjects</h2>
    <p>Before we go on to understand what are the subjects in the science stream after the 10th, a major point to note is that it offers 2 substreams. Non-medical and Medical are the 2 substreams available. If you want to pursue a career in science but not in Medicine, you can opt for a Non-medical stream. On the other hand, if you are certain of kick-starting a career in medicine, you should choose the Medical stream.</p>
    <p>You will be presented with Physics, Chemistry, and Biology (PCB) if you opt for the medical stream. However, Mathematics remains to be optional. For those choosing non-medical; Physics, Chemistry, and Mathematics (PCM) are the mandatory subjects. The non-medical students can choose one more mandatory subject as per their choice. English/Hindi is a compulsory subject for both fields.</p>

    <h2>Science Stream after 10th Subjects</h2>
    <p>Now let us understand in detail what these subjects entail-</p>

    <h3 class="highlight">Physics</h3>
    <p>Physics is defined as the study of the structure of matter and how the tiny particles of the universe interact. The curriculum of Physics for class 11th and 12th focus on the topics like motion in a straight line, units of measurements, thermodynamics, kinetic energy, waves and oscillation, laws of motion, motion in a plane, gravitation, mechanical properties of fluids, thermal properties of matter and solids, laws of motion.</p>

    <h3 class="highlight">Chemistry</h3>
    <p>Class 11th and 12th Chemistry will no longer only revolve around chemical reactions. During these years, you will be taken through various in-depth theories based on molecules, the structure of atoms, and chemical equations. Alongside these topics, many new topics like equilibrium, hydrogen, classification of elements, hydrocarbons, periodicity properties, redox reaction, e-block and p-block elements, thermodynamics, organic chemistry, and states of matter.</p>

    <h3 class="highlight">Mathematics</h3>
    <p>Considered one of the most difficult subjects in classes 11th and 12th, mathematics needs your time and dedication through the journey. Initially, the concepts of this subject will require more focus but, with time you can develop a better hold over the subject. Here are some of the important chapters trigonometric functions, relations and functions, straight lines, linear inequalities, permutations and combinations, probability, statistics, mathematical reasoning, quadratic equations, binomial theorem, conic sections, mathematical induction, limits, and derivatives, and three-dimensional geometry.</p>

    <h3 class="highlight">Biology</h3>
    <p>Biology’s curriculum of these classes will open doors to a new world of the living! The core of class 11th will be Botany, whereas, class 12th will focus on Zoology. The chapters for the 11th and 12th can be numbered, but the study of Biology will be vast. Following are the chapter you must be studying during these years living world, plant and animal kingdom, biological classification, gases, body fluids, excretory products, morphology and anatomy of flowering plants, cells, biomolecules, mineral nutrition, plant respiration, growth and development, chemical coordination.</p>

