    <h3 class="highlight">Central Board of Secondary Education (CBSE)</h3>
    <ul>
        <li>Theory: History of Indian Art, Book Craft, and Fundamentals of Visual Arts</li>
        <li>Practical: Painting, Graphics, Sculpture, and Applied Art</li>
    </ul>

    <h3 class="highlight">Indian Certificate of Secondary Education (ICSE)</h3>
    <ul>
        <li>Theory: Introduction to Indian Art, Commercial Arts, and Drawing and Painting</li>
        <li>Practical: Drawing and Painting, Graphics, and Sculpture</li>
    </ul>

    <h3 class="highlight">State Boards</h3>
    <p>Many state boards in India, such as Maharashtra State Board of Secondary and Higher Secondary Education, offer the Art stream with subjects like Drawing and Painting, Graphics, Sculpture, and Applied Art.</p>

    <h2>Eligibility Criteria</h2>
    <p>To pursue the Art stream in 11th and 12th, students need to have completed their 10th standard from a recognized board. Some schools or institutions may conduct an aptitude test or require a portfolio submission to assess the artistic abilities of the candidates.</p>

